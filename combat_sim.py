def melhor_arma(skill, tabela):
    """Retorna a melhor arma disponível para um dado skill."""
    candidatas = [a for a in tabela if a[-1] <= skill]
    if not candidatas:
        return tabela[0]
    return max(candidatas, key=lambda a: (a[0]+a[1])/2)

def melhor_encaixe(level):
    """Retorna o dano por encaixe para um dado level."""
    candidatos = [e for e in _ENCAIXES if e[1] <= level]
    if not candidatos:
        return 0
    return max(candidatos, key=lambda e: e[0])[0]

def melhor_anel(level):
    """Retorna os bônus do melhor anel para um dado level."""
    candidatos = [a for a in _ANEIS if a[5] <= level]
    if not candidatos:
        return (0,0,0,0,0)
    best = max(candidatos, key=lambda a: sum(a[:5]))
    return best[:5]  # forca, agil, res, ac, blq

def melhor_amuleto(level):
    """Retorna os bônus do melhor amuleto para um dado level."""
    candidatos = [a for a in _AMULETOS if a[5] <= level]
    if not candidatos:
        return (0,0,0,0,0)
    best = max(candidatos, key=lambda a: sum(a[:5]))
    return best[:5]

def melhor_armadura(skill_arm):
    """Retorna (def_min, def_max) da melhor armadura."""
    candidatas = [a for a in _ARMADURAS if a[3] <= skill_arm]
    if not candidatas:
        return (0, 0)
    best = max(candidatas, key=lambda a: (a[0]+a[1])/2)
    return (best[0], best[1])

def melhor_escudo(skill_arm):
    """Retorna (def_min, def_max, blq_bonus) do melhor escudo."""
    candidatos = [e for e in _ESCUDOS if e[5] <= skill_arm]
    if not candidatos:
        return (0, 0, 0)
    best = max(candidatos, key=lambda e: (e[0]+e[1])/2 + e[4])
    return (best[0], best[1], best[4])  # def_min, def_max, blq_bonus

def bonus_forca(dano_medio, forca):
    """Estima bônus de dano pela força."""
    if dano_medio <= 0 or forca <= 0:
        return 0
    # Fórmula: bônus ≈ forca × dano_medio / 55
    # Ajuste para armas fracas (dano < 10): divisor maior
    divisor = max(55, 55 + (10 - dano_medio) * 3) if dano_medio < 10 else 55
    return forca * dano_medio / divisor

def bonus_agilidade(def_media, agilidade):
    """
    Bônus de defesa pela agilidade.
    Calibrado com combates reais: cada ponto de agilidade vale ~0.28 de defesa,
    independente da defesa base (escudo/armadura).
    Agilidade negativa = sem bônus.
    """
    if agilidade <= 0:
        return 0
    return agilidade * 0.28

def simular_combate(eu, adv):
    """
    Simula combate entre dois personagens.
    Retorna dict com dano_eu, dano_adv, rounds_eu, rounds_adv,
    dano_liquido_eu, dano_liquido_adv, vencedor_provavel, score (0-100)
    """
    # ── Atributos base ────────────────────────────────────────────────────────
    eu_lv    = eu.get("level", 1)
    eu_frc   = eu.get("forca", 0)
    eu_res   = eu.get("resistencia", 0)
    eu_agil  = eu.get("agilidade", 0)
    eu_ac    = eu.get("arte_combate", 0)
    eu_blq   = eu.get("bloqueio", 0)
    eu_sk1   = eu.get("sk_1mao", 0)
    eu_sk2   = eu.get("sk_2maos", 0)
    eu_arm   = eu.get("sk_armadura", 0)
    eu_hp    = eu.get("hp", 1000)

    adv_lv   = adv.get("level", 1)
    adv_frc  = adv.get("forca", 0)
    adv_res  = adv.get("resistencia", 0)
    adv_agil = adv.get("agilidade", 0)
    adv_ac   = adv.get("arte_combate", 0)
    adv_blq  = adv.get("bloqueio", 0)
    adv_sk1  = adv.get("sk_1mao", 0)
    adv_sk2  = adv.get("sk_2maos", 0)
    adv_arm  = adv.get("sk_armadura", 0)

    # ── Anéis e Amuleto (bônus por level) ────────────────────────────────────
    # Adversário — usa 2 anéis do melhor disponível
    adv_anel = melhor_anel(adv_lv)
    adv_amu  = melhor_amuleto(adv_lv)
    adv_frc  += adv_anel[0]*2 + adv_amu[0]
    adv_agil += adv_anel[1]*2 + adv_amu[1]
    adv_res  += adv_anel[2]*2 + adv_amu[2]
    adv_ac   += adv_anel[3]*2 + adv_amu[3]
    adv_blq  += adv_anel[4]*2 + adv_amu[4]

    # Eu — usa 2 anéis
    eu_anel  = melhor_anel(eu_lv)
    eu_amu   = melhor_amuleto(eu_lv)
    eu_frc  += eu_anel[0]*2 + eu_amu[0]
    eu_agil += eu_anel[1]*2 + eu_amu[1]
    eu_res  += eu_anel[2]*2 + eu_amu[2]
    eu_ac   += eu_anel[3]*2 + eu_amu[3]
    eu_blq  += eu_anel[4]*2 + eu_amu[4]

    # ── Arma ─────────────────────────────────────────────────────────────────
    if adv_sk2 > adv_sk1:
        adv_arma = melhor_arma(adv_sk2, _ARMAS_2H)
    else:
        adv_arma = melhor_arma(adv_sk1, _ARMAS_1H)

    if eu_sk2 > eu_sk1:
        eu_arma = melhor_arma(eu_sk2, _ARMAS_2H)
    else:
        eu_arma = melhor_arma(eu_sk1, _ARMAS_1H)

    adv_dano_base = (adv_arma[0] + adv_arma[1]) / 2
    eu_dano_base  = (eu_arma[0]  + eu_arma[1])  / 2
    adv_encaixes  = adv_arma[2]
    eu_encaixes   = eu_arma[2]

    # ── Aplica penalidades da arma APENAS no adversário ─────────────────────
    # Os atributos do EU já vêm do status com itens equipados (MY_STATS)
    # O adversário só temos os atributos base — precisamos estimar o impacto da arma
    # Tupla arma: (dano_min, dano_max, encaixes, res_penalidade, agil_penalidade, skill)
    adv_res  = max(0, adv_res  + adv_arma[3])
    adv_agil = adv_agil + adv_arma[4]

    # ── Encaixes ─────────────────────────────────────────────────────────────
    adv_enc_dano = melhor_encaixe(adv_lv) * adv_encaixes
    eu_enc_dano  = melhor_encaixe(eu_lv)  * eu_encaixes

    # ── Dano total por acerto ─────────────────────────────────────────────────
    adv_dano_total = adv_dano_base + bonus_forca(adv_dano_base, adv_frc) + adv_enc_dano
    eu_dano_total  = eu_dano_base  + bonus_forca(eu_dano_base,  eu_frc)  + eu_enc_dano

    # ── Defesa ────────────────────────────────────────────────────────────────
    adv_arm_def = melhor_armadura(adv_arm)
    adv_esc_def = melhor_escudo(adv_arm) if adv_sk1 >= adv_sk2 else (0, 0, 0)
    adv_def_base = (adv_arm_def[0]+adv_arm_def[1])/2 + (adv_esc_def[0]+adv_esc_def[1])/2
    adv_def_total = adv_def_base + bonus_agilidade(adv_def_base, adv_agil)
    adv_blq += adv_esc_def[2] if len(adv_esc_def) > 2 else 0

    eu_arm_def  = melhor_armadura(eu_arm)
    eu_esc_def  = melhor_escudo(eu_arm) if eu_sk1 >= eu_sk2 else (0, 0, 0)
    eu_def_base = (eu_arm_def[0]+eu_arm_def[1])/2 + (eu_esc_def[0]+eu_esc_def[1])/2
    eu_def_total = eu_def_base + bonus_agilidade(eu_def_base, eu_agil)
    eu_blq += eu_esc_def[2] if len(eu_esc_def) > 2 else 0

    # ── Taxa de acerto ────────────────────────────────────────────────────────
    taxa_eu  = eu_ac  / (eu_ac  + adv_blq) if (eu_ac + adv_blq) > 0 else 0.5
    taxa_adv = adv_ac / (adv_ac + eu_blq)  if (adv_ac + eu_blq) > 0 else 0.5

    # ── Rounds — cada um tem o seu número baseado na própria resistência ────────
    # Ex: eu res=45 → 15 ataques, adv res=10 → 4 ataques (independentes)
    rounds_eu  = res_to_rounds(eu_res)
    rounds_adv = res_to_rounds(adv_res)

    # ── Dano líquido por ataque ───────────────────────────────────────────────
    # Taxa de acerto já é a probabilidade de acertar por ataque
    # Dano liquido = (dano_bruto - defesa_adv) × taxa_acerto
    dano_liq_eu  = max(0, eu_dano_total  - adv_def_total) * taxa_eu
    dano_liq_adv = max(0, adv_dano_total - eu_def_total)  * taxa_adv

    # ── Dano total no combate — cada um usa seus próprios rounds ──────────────
    total_eu  = dano_liq_eu  * rounds_eu
    total_adv = dano_liq_adv * rounds_adv

    # ── Score de vitória (0-100) ──────────────────────────────────────────────
    if total_eu + total_adv > 0:
        raw_score = total_eu / (total_eu + total_adv) * 100
    else:
        raw_score = 50

    score = max(0, min(100, round(raw_score)))

    return {
        "score":         score,
        "total_eu":      round(total_eu, 1),
        "total_adv":     round(total_adv, 1),
        "dano_eu":       round(eu_dano_total, 1),
        "dano_adv":      round(adv_dano_total, 1),
        "taxa_eu":       round(taxa_eu * 100, 1),
        "taxa_adv":      round(taxa_adv * 100, 1),
        "rounds_eu":     rounds_eu,
        "rounds_adv":    rounds_adv,
        "def_eu":        round(eu_def_total, 1),
        "def_adv":       round(adv_def_total, 1),
        "arma_eu":       f"{eu_arma[0]}-{eu_arma[1]}",
        "arma_adv":      f"{adv_arma[0]}-{adv_arma[1]}",
        "vencedor":      "eu" if score >= 50 else "adv",
    }


